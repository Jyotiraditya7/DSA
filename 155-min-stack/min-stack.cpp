/*
1)O(1) time and O(2N) space approach:
create single stack and store pair of el along with curr min el till the index
for pop return top.first
for push push the el+min(el,min_before)
for min return top.second
2) O(1) time and O(N) space approach:
*/

class MinStack {
  stack < pair < int, int >> st;

  public:
    void push(int x) {
      int min;
      if (st.empty()) {
        min = x;
      } else {
        min = std::min(st.top().second, x);
      }
      st.push({x,min});
    }

  void pop() {
    st.pop();
  }

  int top() {
    return st.top().first;
  }

  int getMin() {
    return st.top().second;
  }
};

